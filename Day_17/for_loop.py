#for in list

students = ["anubha","tanisha","vanshita"]
for y in students:
    print(y)
    
#Looping through a string

for x in "Vanshita":
    print(x)
    

#The break statement

fruits = ["Guava","Cherry","Tomato"];
for x in fruits:
    print(x)
    if x == "Cherry":
        break
    
    
pickle = ["mango","carrot","garlic"];
for p in pickle:
    if p == "carrot":
        break
    print(p)